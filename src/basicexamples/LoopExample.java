package basicexamples;

public class LoopExample {
    public static void main(String[] args) {
        int i = 0; // zadefinovali sme premennu i hodnotu 0
        while (i < 5) {
            System.out.println(i);
            i++; //inkrementuje premennu i,cize zväčší o +1
        }
        // cyklus bude pokračovať TU
    }
}
